int	ft_str_is_alpha(char *str)
{
	int	i;
	int	lower;

	i = 0;
	lower = 1;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!('a' <= str[i] && str[i] <= 'z'))
			lower = 0;
		if (!('A' <= str[i] && str[i] <= 'Z'))
		{
			if (!(lower))
			{
				return (0);
			}
		}
		lower = 1;
		i++;
	}
	return (1);
}
